package Day5;
public class EvenOdd {
    public static void main(String[] args) {
        int input = 10; // Change this number to check different values

        if (input % 2 == 0) {
            System.out.println(input + " is an even number.");
        } else {
            System.out.println(input + " is an odd number.");
        }
    }
}
